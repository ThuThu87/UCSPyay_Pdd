	ရခိုင်ဒေသ၌ ဝါကျွတ်လတွင်ကျင်းပသော ပွဲတော်များအနက် ဆွမ်းထောင်၊ ပန်းထောင်၊ ဆီးမီးထောင် ပူဇော်ပွဲသည်လည်း ရခိုင်လူမျိုးတို့၏ ထင်ရှားသော ဘာသာရေး ပွဲတော်တစ်ခုဖြစ်ပါသည်။ ထိုသို့ကပ်လှူပူဇော်ခြင်းကို ဓညဝတီခေတ် စန္ဒာသူရိယဘုရင် မင်းကြီးလက်ထက်တွင် ဝါကျွတ်လပြည့်နေ့ရောက်တိုင်း မဟာမြတ်မုနိဘုရားကြီးအား ဆွမ်း၊ ဆီမီး၊ ပန်းတစ်ထောင်စီတို့ဖြင့် လှူဒါန်းပူဇော်လာခဲ့ကြရာမှ စတင်လာခဲ့ခြင်းဖြစ်သည်ဟု သမိုင်းမှတ်တမ်းများအရသိရှိရပါသည်။ ဝါကျွတ်လ ကောက်ဦးစပါးပေါ်ချိန်တွင် ကပ်လှူ ပူဇော်သောအနေဖြင့် ကျင်းပခြင်းဖြစ်ပြီး ယခုမျက်မှောက်ခေတ်တွင်လည်း ရိုးရာအစဉ် အလာမပျက် ကျင်းပလျက်ပင်ရှိပါသည်။
