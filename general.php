	ဆယ့်နှစ်လရာသီလင်္ကာတစ်ပိုဒ်တွင် “မယ်လျှင်စစ်မှာ စိတ်ကြီးပျက်လို့၊ ကျင်ကိုင် လက်ပန်း၊ အခန်းခန်းကို၊ အိမ်သူအကျွန်၊ မြင်ရပြန်ကေ၊ လွမ်းတော်တာရှည်၊ ပန်းမဉ္ဇူကို၊ နီပူထိုးကေ၊ ညှိုးလတ်တေလည်း” ဟူ၍ ရေးဖွဲ့ဆိုထားပြန်သည်။
